package org.apache.servicemix.examples.cxf.service;

public class Service {
	private String name;
	private int weigh;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getWeigh() {
		return weigh;
	}

	public void setWeigh(int weigh) {
		this.weigh = weigh;
	}
}
